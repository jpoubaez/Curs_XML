Les teves dades podran ser accedits per aquells proveïdors que presten serveis a JoanP, com ara serveis d'allotjament, eines de màrqueting i sistemes de contingut o altres professionals, quan la comunicació sigui necessària normativament, o per a l'execució dels serveis contractats.

JoanP, ha subscrit els corresponents contractes d'encàrrec de tractament amb cada un dels proveïdors que presten serveis a JoanP, amb l'objectiu de garantir que aquests proveïdors tractaran les dades de conformitat amb el que estableix la legislació vigent.

També podran ser cedides a les Forces i Cossos de Seguretat de l'Estat en els casos que hi hagi una obligació legal.

Bancs i entitats financeres, per al cobrament dels serveis.
Administracions públiques amb competència en els sectors d'activitat, quan així ho estableixi la normativa vigent.
